const assert = require('node:assert');

const bytes = new Uint8Array([
  0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00,
  0x01, 0x09, 0x02, 0x60, 0x01, 0x7f, 0x00, 0x60, 0x00, 0x01, 0x7f,
  0x02, 0x10, 0x01, 0x03, 0x65, 0x6e, 0x76, 0x06, 0x6d, 0x65, 0x6d, 0x6f,
  0x72, 0x79, 0x02, 0x01, 0x01, 0x02,
  0x03, 0x03, 0x02, 0x00, 0x01,
  0x07, 0x10, 0x02, 0x05, 0x73, 0x74, 0x6f, 0x72, 0x65, 0x00, 0x00, 0x04,
  0x6c, 0x6f, 0x61, 0x64, 0x00, 0x01,
  0x0a, 0x13, 0x02, 0x09, 0x00, 0x41, 0x00, 0x20, 0x00, 0x36, 0x02, 0x00,
  0x0b, 0x07, 0x00, 0x41, 0x00, 0x28, 0x02, 0x00, 0x0b,
]);

const memory = new WebAssembly.Memory({ initial: 1, maximum: 2 });
const instance = new WebAssembly.Instance(new WebAssembly.Module(bytes), {
  env: { memory },
});

const view = new DataView(memory.buffer);
instance.exports.store(0x78563412);
assert.strictEqual(view.getUint32(0, true), 0x78563412);

view.setUint32(0, 0x10203040, true);
assert.strictEqual(instance.exports.load(), 0x10203040);

assert.strictEqual(memory.grow(1), 1);
new DataView(memory.buffer).setUint32(0, 0x55667788, true);
assert.strictEqual(instance.exports.load(), 0x55667788);

console.log('wasm:imported-memory-shared:ok');
